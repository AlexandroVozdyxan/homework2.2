# // - ціла частина від розподілу 2333 // 1000 = 2
# % - залишок від ділення 3332 % 10 = 2
# ввести з клавіатури 5-ти значне число та вивести всі цифри цього числа
# число не має містити 0 на початку
# використовувати % та // для вирішення
# цифри цього числа мають відобразитися в зворотньому напрямку
number = 83482
n1 = number // 10000
n2 = number // 1000 % 10
n3 = number // 100 % 10
n4 = number % 100 // 10
n5 = number % 10
print(n5)
print(n4)
print(n3)
print(n2)
print(n1)